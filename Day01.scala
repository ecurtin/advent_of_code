  def whichFloor(input: String): Int = {
    val list = input.toList
    // val nums = list.flatMap( paren => if (paren == "(") 1 else -1)
    val nums = list.collect{
      case '(' => 1
      case ')' => -1
    }
    nums.sum
  }

